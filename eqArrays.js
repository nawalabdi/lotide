const eqArrays = function (arr1, arr2) {
  if (!Array.isArray(arr1) || !Array.isArray(arr2)) {
    return false
  }

  if (arr1.length !== arr2.length) {
    return false
  }

  for (let b = 0; b < arr1.length; b++) {
    if (arr1[b] !== arr2[b]) return false
  }

  return true
}

